public class UsingIfStatements {
	public static void main(String[] args) {

		int x = 10;
		int y = 20;
		
		if (x < y) {
			System.out.println("x is less than y");
		}

		if (x > y) {
			System.out.println("x is greater than y");
		}
		if (x == y) {
			System.out.println("x is equals to y");
		}
	}
}